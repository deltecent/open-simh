/*
 * Reset function for the main JAIR device.
 *
 * Enables all other Sol-20 devices
 */
static t_stat ide_reset(DEVICE *dptr)
{
    static t_bool first = TRUE;

    if (dptr->flags & DEV_DIS) { /* Disconnect Resources */
        sim_map_resource(ide_ctx.io_base, ide_ctx.io_size, RESOURCE_TYPE_IO, &ide_io, "ideio", TRUE);
        ide_set_norom(NULL, 0, NULL, NULL);
    }
    else {
        /* Connect I/O Ports at base address */
        if (sim_map_resource(ide_ctx.io_base, ide_ctx.io_size, RESOURCE_TYPE_IO, &ide_io, "ideio", FALSE) != 0) {
            sim_debug(ERROR_MSG, &ide_dev, "Error mapping I/O resource at 0x%02x\n", ide_ctx.io_base);
            return SCPE_ARG;
        }
        ide_set_rom(NULL, 0, NULL, NULL);
    }

    sim_debug(STATUS_MSG, &ide_dev, "reset controller.\n");

    return SCPE_OK;
}

