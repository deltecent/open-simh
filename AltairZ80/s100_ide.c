#define IDE_DRIVE     0x34        /* BIT 0=DRIVE-A 1=DRIVE-B */

// READcfg8255     EQU     10010010b ;Set 8255 IDEportC to output, IDEportA/B input
// WRITEcfg8255    EQU     10000000b ;Set all three 8255 ports to output mode

/* IDE control lines for use with IDEportC. */

#define IDE_A0LINE    0x01     /* direct from 8255 to IDE interface */
#define IDE_A1LINE    0x02     /* direct from 8255 to IDE interface */
#define IDE_A2LINE    0x04     /* direct from 8255 to IDE interface */
#define IDE_CS0LINE   0x08     /* inverter between 8255 and IDE interface */
