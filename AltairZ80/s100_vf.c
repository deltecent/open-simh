/* Reset routine */
static t_stat vf_reset(DEVICE *dptr)
{
    PNP_INFO *pnp = (PNP_INFO *)dptr->ctxt;
    DEVICE *wd179x = NULL;

    if(dptr->flags & DEV_DIS) { /* Disconnect ROM and I/O Ports */
        /* Unmap I/O Ports */
        sim_map_resource(pnp->io_base, pnp->io_size, RESOURCE_TYPE_IO, &vf_control, "vf_control", TRUE);
        /* Unmap PROM */
        sim_map_resource(pnp->mem_base, pnp->mem_size, RESOURCE_TYPE_MEMORY, &vf_ddbios, "vf_ddbios", TRUE);
    } else {
        /* Connect VF Disk Flags and Control Register */
        if(sim_map_resource(pnp->io_base, pnp->io_size, RESOURCE_TYPE_IO, &vf_control, "vf_control", FALSE) != 0) {
            sim_printf("%s: error mapping I/O resource at 0x%04x\n", __FUNCTION__, pnp->io_base);
            return SCPE_ARG;
        }
        /* Connect VF DDBIOS PROM */
        if(sim_map_resource(pnp->mem_base, pnp->mem_size, RESOURCE_TYPE_MEMORY, &vf_ddbios, "vf_ddbios", FALSE) != 0) {
            sim_printf("%s: error mapping MEM resource at 0x%04x\n", __FUNCTION__, pnp->mem_base);
            return SCPE_ARG;
        }

        /* Enable WD179x device */
        if (wd179x == NULL) {
            if ((wd179x = find_dev("WD179X")) != NULL) {
                set_dev_enbdis(wd179x, NULL, 1, NULL);
            }
        }
    }

    return SCPE_OK;
}

/* VersaFloppy Disk Control/Status */
static int32 vf_control(const int32 port, const int32 io, const int32 data)
{
    uint8_t inv;
    int32 result = 0;
    if(io) { /* I/O Write */
        vf_info_data.creg = data;
        inv = ~(data & 0xff);
        for (wd179x_infop->sel_drive = 0; wd179x_infop->sel_drive < VF_MAX_DRIVES; wd179x_infop->sel_drive++) {
            if (inv & (1 << wd179x_infop->sel_drive)) {
                break;
            }
        }
        wd179x_infop->drivetype = (inv & 0x20) ? 5 : 8;
        wd179x_infop->fdc_head = (inv & 0x10) >> 4;
        wd179x_infop->ddens = (inv & 0x40) >> 6;

        sim_debug(DRIVE_MSG, &vf_dev, VF_SNAME ": " ADDRESS_FORMAT " WR CTRL(0x%02x)  = 0x%02x: Drive: %d, Head: %d, %s-Density.\n",
            PCX, port,
            data & 0xFF,
            wd179x_infop->sel_drive,
            wd179x_infop->fdc_head,
            wd179x_infop->ddens == 1 ? "Double" : "Single");
    } else { /* I/O Read */
        result = vf_info_data.creg;
        sim_debug(STATUS_MSG, &vf_dev, VF_SNAME ": " ADDRESS_FORMAT
            " Read Control, Port 0x%02x Result 0x%02x\n", PCX, port, result);
    }

    return result;
}

